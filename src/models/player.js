const { DataTypes } = require("sequelize");
const sequelize = require("../database");

const Player = sequelize.define("Player", {
  fullName: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false,
  },
});

module.exports = Player;
